package week5;

public class RangeNumberClass {
	public static int sumof1To10() {
		int init = 1, sum = 1;
		while(init < 10) {
			sum += init;
			sum ++;
			init ++;
		}
		return sum;
	}
	
	public static int sumof20To30() {
		int init = 20, sum = 20;
		while(init < 30) {
			sum += init;
			sum ++;
			init ++;
		}
		return sum;
	}
	
	public static int sumof35To45() {
		int init = 35, sum = 35;
		while(init < 45) {
			sum += init;
			sum ++;
			init ++;
		}
		return sum;
	}

}
